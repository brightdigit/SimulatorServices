import Foundation

/// Identifier for a device or runtime.
public typealias Identifier = String

/// State for the device
public typealias DeviceState = String

/// State for an Apple Watch and iPhone pair
public typealias PairState = String

/// Device's product family.
public typealias ProductFamily = String

/// Device platform
public typealias Platform = String

/// Operating System Version
public typealias Version = String

/// Device Model ID
public typealias ModelID = String

/// Device Type Identifier
public typealias DeviceTypeID = Identifier

/// Runtime ID
public typealias RuntimeID = Identifier

/// Path to the file
public typealias Path = String
